    }
    public class var rickAndMortyTitleGreen: UIColor {
        return UIColor(red: 112/255, green: 205/255, blue: 23/255, alpha: 1.0)
    }
    public class var rickAndMortyPortalYellow: UIColor {
        return UIColor(red: 218/255, green: 230/255, blue: 49/255, alpha: 1.0)
    }
    public class var mainColor: UIColor {
        return UIColor(red: 47/255, green: 54/255, blue: 64/255, alpha: 1.0)
    }
    public class var mainBackgroundColor: UIColor {
        return UIColor(red: 56/255, green: 65/255, blue: 78/255, alpha: 1.0)
    }
}
